# How to get Data type of the object
obj=1
print(type(obj))
#output : <class 'int'>

obj=2.0
print(type(obj))
#output :<class 'float'>

obj="Hello"
print(type(obj))
#output :<class 'str'>

obj=2+9j
print(type(obj))
#output :<class 'complex'>

obj=2+9J
print(type(obj))
#output :<class 'complex'>

obj=2+9.0
print(type(obj))
#output :<class 'float'>

obj=9.
print(type(obj))
#output :<class 'float'>
